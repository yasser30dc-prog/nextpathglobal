// Helper functions
export function getInstitutionBySlug(slug: string): Institution | undefined {
    return institutions.find(inst => inst.slug === slug);
}

export function getInstitutionsByType(type: InstitutionType): Institution[] {
    return institutions.filter(inst => inst.type === type);
}

export function getInstitutionsByCategory(category: InstitutionCategory): Institution[] {
    return institutions.filter(inst => inst.category === category);
}

export function getUniversities(): Institution[] {
    return institutions.filter(inst => inst.category === 'university');
}

export function getColleges(): Institution[] {
    return institutions.filter(inst => inst.category === 'college');
}

export function getUniversityColleges(): Institution[] {
    return institutions.filter(inst => inst.category === 'university-college');
}

export function getPublicUniversities(): Institution[] {
    return institutions.filter(inst => inst.type === 'public');
}

export function getPrivateUniversities(): Institution[] {
    return institutions.filter(inst => inst.type === 'private' && inst.category === 'university');
}

export function getForeignBranchCampuses(): Institution[] {
    return institutions.filter(inst => inst.type === 'foreign-branch');
}
